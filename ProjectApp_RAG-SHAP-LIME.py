def save_feedback(message, response, rating, comment):
    st.session_state.feedback_db.append({
        "timestamp": datetime.now(),
        "message": message,
        "response": response,
        "rating": rating,
        "comment": comment
    })
    st.session_state.metrics["total_feedback"] += 1
    load_feedback_data.clear()

