def generate_response(message: str, temperature: float):
    start = time.time()
    client = load_llm()

    if st.session_state.vectordb is None:
        return (
            "Please upload a PDF document first.",
            None,
            0.0
        )

    retriever = st.session_state.vectordb.as_retriever(
        search_kwargs={"k": 4}
    )

    docs = retriever.invoke(message)
    chunks = [d.page_content for d in docs]

    #SHAP
def shap_explanation(chunks: List[str], question: str):
    """
    SHAP over semantic similarity between chunks and question
    """

    embeddings = OpenAIEmbeddings()

    chunk_embeddings = embeddings.embed_documents(chunks)
    question_embedding = embeddings.embed_query(question)

    def similarity_model(x):
        sims = []
        for emb in chunk_embeddings:
            sims.append(np.dot(emb, question_embedding))
        return np.array(sims)

    explainer = shap.Explainer(similarity_model, chunks)
    shap_values = explainer(chunks)

    return shap_values

    #
    #LIME
def lime_explanation(chunks: List[str], question: str):
    explainer = LimeTextExplainer(class_names=["relevant"])

    def predictor(texts):
        embeddings = OpenAIEmbeddings()
        q_emb = embeddings.embed_query(question)

        scores = []
        for t in texts:
            t_emb = embeddings.embed_query(t)
            scores.append([np.dot(t_emb, q_emb)])

        return np.array(scores)

    exp = explainer.explain_instance(
        " ".join(chunks),
        predictor,
        num_features=5
    )

    return exp
    #

    if not docs:
        context = ""
    else:
        context = "\n\n".join(d.page_content for d in docs)

    system_prompt = (
        "You are a strict AI assistant.\n"
        "Answer ONLY using the context provided.\n"
        "If the answer is not in the context, say:\n"
        "'I cannot find that information in the provided document.'"
    )

    messages = [
        {"role": "system", "content": system_prompt},
        {
            "role": "user",
            "content": f"Context:\n{context}\n\nQuestion:\n{message}"
        }
    ]

    response = client.chat.completions.create(
        model="gpt-3.5-turbo",
        messages=messages,
        temperature=temperature,
        max_tokens=st.session_state.preferences["max_tokens"]
    )

    output = response.choices[0].message.content
    explanation = compute_explanation(message, output)
    elapsed = time.time() - start

    return output, explanation, elapsed

#Codigo para que pida pdf
@st.cache_resource
def process_pdf(uploaded_file):
    reader = PdfReader(uploaded_file)

    text = ""
    for page in reader.pages:
        page_text = page.extract_text()
        if page_text:
            text += page_text

    splitter = RecursiveCharacterTextSplitter(
        chunk_size=1000,
        chunk_overlap=200
    )

    docs = splitter.create_documents([text])

    embeddings = OpenAIEmbeddings()
    vectordb = Chroma.from_documents(docs, embeddings)

    return vectordb

